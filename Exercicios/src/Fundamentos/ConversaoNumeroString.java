package Fundamentos;

public class ConversaoNumeroString{
    public static void main(String[] args) {
        Integer num1 = 10000;// Interge oferece varias funcionalidades dentro do java
        System.out.println(num1.toString().length());//convetendo inteiro pra String com interge(função .toString())
    
        //Convertendo o numero inteiro em String com interge na saida do programa.
        int num2 = 7777777;
        System.out.println(Integer.toString(num2).length());
    
    
    }
}